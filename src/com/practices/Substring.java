package com.practices;

public class Substring {

	public static void main(String[] args) {
		
		String name = "Mahesh and Sam";
		String nameHalf = name.substring(7);
		System.out.println(nameHalf);
		
		String abc = "Ganesh Tripathi";
		String abcHalf = abc.substring(1, 6);
		System.out.println(abcHalf);

	}

}
